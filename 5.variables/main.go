package main

import "fmt"

func main() {
	var myvariable = 20
	var myvariable1 = 33.33
	var myvariable2 = "hello1"
	fmt.Printf("The value of my variable is: %d\n", myvariable)
	fmt.Printf("The type of my variable is: %T\n", myvariable)
	fmt.Printf("The value of my variable is: %f\n", myvariable1)
	fmt.Printf("The value of my variable is: %s\n", myvariable2)
}
